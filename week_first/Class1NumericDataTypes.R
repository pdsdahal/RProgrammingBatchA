#Numeric Data Types 

#to represent integer use the letter L after the value assigned for e.g.

#Integer
age <- 11L
is.numeric(age) #TRUE
is.integer(age) #TRUE
typeof(age) #to check the data type of a variable
class(age) #to check the data type of a variable


#double
num1 <- 10
is.numeric(num1) #TRUE
is.integer(num1) #FALSe
typeof(num1) #double
